export function AdminSidebar({ userEmail }: { userEmail: string }) {
  const pathname = usePathname();
  const [mobileOpen, setMobileOpen] = useState(false);

  function isActive(href: string, exact?: boolean) {
    if (exact) return pathname === href;
    return pathname.startsWith(href);
  }

  const sidebarContent = (
    <>
      {/* Logo / Title */}
      <div className="p-6 border-b border-[var(--border-glass)]">
        <Link href="/dashboard/admin" className="block">
          <span className="text-lg font-extrabold tracking-tight gradient-text">
            dumbroof<span className="font-normal opacity-70">.ai</span>
          </span>
          <p className="text-xs text-[var(--gray-dim)] mt-1">Command Center</p>
        </Link>
      </div>

      {/* Nav Items */}
      <nav className="flex-1 py-4 px-3 space-y-1">
        {navItems.map((item) => {
          const active = isActive(item.href, item.exact);
          return (
            <Link
              key={item.href}
              href={item.href}
              onClick={() => setMobileOpen(false)}
              className={`flex items-center gap-3 px-3 py-2.5 rounded-xl text-sm font-medium transition-colors ${
                active
                  ? "bg-gradient-to-r from-[var(--pink)]/20 to-[var(--blue)]/20 text-[var(--white)] border border-[var(--border-glass-active)]"
                  : "text-[var(--gray)] hover:text-[var(--white)] hover:bg-white/[0.04]"
              }`}
            >
              <span className={active ? "text-[var(--cyan)]" : "text-[var(--gray-dim)]"}>
                {item.icon}
              </span>
              {item.label}
            </Link>
          );
        })}
      </nav>

      {/* Bottom section */}
      <div className="p-4 border-t border-[var(--border-glass)]">
        <p className="text-xs text-[var(--gray-dim)] truncate mb-3">{userEmail}</p>
        <Link
          href="/dashboard"
          onClick={() => setMobileOpen(false)}
          className="flex items-center gap-2 px-3 py-2 rounded-xl text-sm text-[var(--gray)] hover:text-[var(--white)] hover:bg-white/[0.04] transition-colors"
        >
          <svg className="w-4 h-4" fill="none" viewBox="0 0 24 24" stroke="currentColor" strokeWidth={1.5}>
            <path strokeLinecap="round" strokeLinejoin="round" d="M10.5 19.5L3 12m0 0l7.5-7.5M3 12h18" />
          </svg>
          Back to Dashboard
        </Link>
      </div>
    </>
  );

  return (
    <>
      {/* Desktop sidebar — fixed left */}
      <aside className="hidden lg:flex flex-col fixed left-0 top-0 bottom-0 w-[240px] bg-[rgba(6,9,24,0.92)] backdrop-blur-[20px] border-r border-[var(--border-glass)] z-40">
        {sidebarContent}
      </aside>

      {/* Mobile toggle button */}
      <button
        onClick={() => setMobileOpen(!mobileOpen)}
        className="lg:hidden fixed top-4 left-4 z-50 p-2 rounded-xl bg-[rgb(15,18,35)] border border-[var(--border-glass)] text-[var(--gray)] hover:text-[var(--white)] transition-colors"
        aria-label="Toggle admin menu"
      >
        {mobileOpen ? (
          <svg className="w-5 h-5" fill="none" viewBox="0 0 24 24" stroke="currentColor" strokeWidth={2}>
            <path strokeLinecap="round" strokeLinejoin="round" d="M6 18L18 6M6 6l12 12" />
          </svg>
        ) : (
          <svg className="w-5 h-5" fill="none" viewBox="0 0 24 24" stroke="currentColor" strokeWidth={2}>
            <path strokeLinecap="round" strokeLinejoin="round" d="M3.75 6.75h16.5M3.75 12h16.5m-16.5 5.25h16.5" />
          </svg>
        )}
      </button>

      {/* Mobile overlay */}
      {mobileOpen && (
        <div
          className="lg:hidden fixed inset-0 bg-black/60 z-40"
          onClick={() => setMobileOpen(false)}
        />
      )}

      {/* Mobile sidebar — slide in */}
      <aside
        className={`lg:hidden fixed left-0 top-0 bottom-0 w-[240px] bg-[rgb(15,18,35)] border-r border-[var(--border-glass)] z-50 flex flex-col transition-transform duration-300 ${
          mobileOpen ? "translate-x-0" : "-translate-x-full"
        }`}
      >
        {sidebarContent}
      </aside>
    </>
  );
}
